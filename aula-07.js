// Operadores de comparação

console.log(5 == 4) // Igual (==) -> Verifica se o valor é igual
console.log(5 != 4) // Diferente ou não igual (!=) -> Verifica se o valor é diferente ou nào igual
console.log(5 === '5') // Estritamente igual (===) -> Verifica se o valor e o tipo são iguais
console.log(5 !== '5') // Estritamente não igual (!==) -> Verifica se o valor ou o tipo é diferente
console.log(6 > 5) // Maior (>) -> Verifica se o valor da esquerda é maior que o da direita
console.log(5 < 6) // Menor (<) -> Verifica se o valor da esquerda é menor que o da direita
console.log(5 >= 5) // Maior ou igual (>=) -> Verifica se o valor da esquerda é maior ou igual o da direita
console.log(5 <= 4) // Menor ou giual (<=) -> Verifica se o valor da esquerda é menor ou igual o da direita

// Operadores lógicos

let x = true
