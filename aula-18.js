// propriedade length (Quantidade de caracteres de uma palávra ou frase)

const variavel = 'Olá, mundo'
console.log(variavel.length)

// método charAt (Retorna um caracter em uma determina posicao -)
// Observação: posição inicia do 0
console.log(variavel.charAt(6))
console.log(variavel.charAt(7))

// colcheter (Retorna um caracter de uma determina posicao -)
// Observação: posição inicia do 0
console.log(variavel[8])
console.log(variavel[9])
